package day64;
public class Array {
    public static void main(String[] args) {
        int[]nums={1,2,3,4,5};

        // for loop
        for(int i=0; i<nums.length; i++){
            System.out.println(nums[i]);
        }
        // forEach loop
        for(int num:nums){
            System.out.println(num);
        }

        
    }
    
}
